// –ü–æ–ª—É—á–∏—Ç—å —Å–µ–≥–æ–¥–Ω—è—à–Ω–∏–π seed
function getTodaySeed(): number {
  const today = new Date()
  return today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
}

// –ü–æ–ª—É—á–∏—Ç—å –∏–Ω–¥–µ–∫—Å –¥–ª—è –∑–Ω–∞–∫–∞ –Ω–∞ —Å–µ–≥–æ–¥–Ω—è
