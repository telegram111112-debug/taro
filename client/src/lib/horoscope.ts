    love: horoscopeDatabase.love[sign][loveIndex],
    career: '–ü—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—ã–µ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏—è —Å–µ–≥–æ–¥–Ω—è –∑–∞–≤–∏—Å—è—Ç –æ—Ç —Ç–≤–æ–µ–≥–æ –Ω–∞—Å—Ç—Ä–æ—è.',
