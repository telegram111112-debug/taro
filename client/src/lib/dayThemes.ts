export function getDayTheme(dayOfWeek: number): DayTheme {
  return dayThemes[dayOfWeek] || dayThemes[0]
}

// CSS –≥—Ä–∞–¥–∏–µ–Ω—Ç –∏–∑ —Ç–µ–º—ã –¥–Ω—è
export function getDayGradient(theme: DayTheme): string {
  if (theme.gradient.via) {
    return `linear-gradient(180deg, ${theme.gradient.from} 0%, ${theme.gradient.via} 50%, ${theme.gradient.to} 100%)`
  }
  return `linear-gradient(180deg, ${theme.gradient.from} 0%, ${theme.gradient.to} 100%)`
}

