// Получить случайное приветствие в зависимости от времени суток
export function getRandomGreeting(): string {
  const hour = new Date().getHours()
  let pool: string[]

  if (hour >= 6 && hour < 12) {
    pool = greetings.morning
  } else if (hour >= 12 && hour < 18) {
    pool = greetings.afternoon
  } else if (hour >= 18 && hour < 23) {
    pool = greetings.evening
  } else {
    pool = greetings.night
  }

  return pool[Math.floor(Math.random() * pool.length)]
}

// Получить простое приветствие (без магии) - для формальных случаев
export function getSimpleGreeting(): string {
  const hour = new Date().getHours()
  if (hour >= 6 && hour < 12) return 'Доброе утро,'
  if (hour >= 12 && hour < 18) return 'Добрый день,'
  if (hour >= 18 && hour < 23) return 'Добрый вечер,'
  return 'Доброй ночи,'
}

// Получить магическое приветствие
export function getMagicalGreeting(): string {
  const hour = new Date().getHours()
  let pool: string[]

  if (hour >= 6 && hour < 12) {
    pool = greetings.morning.slice(5) // Только магические (после 5)
  } else if (hour >= 12 && hour < 18) {
    pool = greetings.afternoon.slice(5)
  } else if (hour >= 18 && hour < 23) {
    pool = greetings.evening.slice(5)
  } else {
    pool = greetings.night.slice(5)
  }

  return pool[Math.floor(Math.random() * pool.length)]
}

