// Функция для получения случайного текста
export function getRandomPreparationText(): PreparationText {
  const randomIndex = Math.floor(Math.random() * preparationTexts.length)
  return preparationTexts[randomIndex]
}

// Функция для получения текста на основе дня недели (дополнительная вариативность)
export function getPreparationTextByDay(): PreparationText {
  const today = new Date()
  const dayOfWeek = today.getDay()
  const dayOfMonth = today.getDate()

  // Комбинируем день недели и день месяца для уникальности
  const index = (dayOfWeek * 7 + dayOfMonth) % preparationTexts.length
  return preparationTexts[index]
}

// Функция для получения текста с учётом времени суток
export function getPreparationTextByTime(): PreparationText {
  const hour = new Date().getHours()
  const baseIndex = Math.floor(Math.random() * preparationTexts.length)

  // Утро (6-12): более энергичные тексты
  if (hour >= 6 && hour < 12) {
    const morningIndices = [5, 6, 7, 20, 21, 22, 23, 24] // индексы позитивных/энергичных текстов
    const adjustedIndex = morningIndices[baseIndex % morningIndices.length]
    return preparationTexts[adjustedIndex]
  }

  // День (12-18): мудрые и философские
  if (hour >= 12 && hour < 18) {
    const afternoonIndices = [15, 16, 17, 18, 19, 35, 36, 37, 38, 39] // мудрые тексты
    const adjustedIndex = afternoonIndices[baseIndex % afternoonIndices.length]
    return preparationTexts[adjustedIndex]
  }

  // Вечер (18-23): мистические и романтические
  if (hour >= 18 && hour < 23) {
    const eveningIndices = [10, 11, 12, 13, 14, 30, 31, 32, 33, 34, 45, 46, 47, 48, 49]
    const adjustedIndex = eveningIndices[baseIndex % eveningIndices.length]
    return preparationTexts[adjustedIndex]
  }

  // Ночь (23-6): нежные и заботливые
  const nightIndices = [0, 1, 2, 3, 4, 25, 26, 27, 28, 29]
  const adjustedIndex = nightIndices[baseIndex % nightIndices.length]
  return preparationTexts[adjustedIndex]
}

// Тексты по времени суток
const morningTexts: number[] = [5, 6, 7, 8, 9, 20, 21, 22, 23, 24, 35, 36, 37, 40, 41, 42] // позитивные, энергичные
const afternoonTexts: number[] = [0, 1, 2, 3, 4, 15, 16, 17, 18, 19, 30, 31, 32, 33, 34] // мудрые, философские
const eveningTexts: number[] = [10, 11, 12, 13, 14, 25, 26, 27, 28, 29, 43, 44, 45, 46, 47] // романтические, мистические
const nightTexts: number[] = [0, 1, 2, 3, 4, 25, 26, 27, 28, 29, 45, 46, 47, 48, 49] // нежные, спокойные

// Основная функция - комбинирует разные подходы для максимальной уникальности
export function getUniquePreparationText(): PreparationText {
  const today = new Date()
  const hour = today.getHours()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()

  // Выбираем пул текстов в зависимости от времени суток
  let textPool: number[]
  if (hour >= 6 && hour < 12) {
    textPool = morningTexts // Утро (6:00 - 11:59)
  } else if (hour >= 12 && hour < 18) {
    textPool = afternoonTexts // День (12:00 - 17:59)
  } else if (hour >= 18 && hour < 23) {
    textPool = eveningTexts // Вечер (18:00 - 22:59)
  } else {
    textPool = nightTexts // Ночь (23:00 - 5:59)
  }

  // Используем seed для псевдо-случайного, но стабильного выбора в течение дня
  const poolIndex = seed % textPool.length
  const textIndex = textPool[poolIndex]

  return preparationTexts[textIndex]
}
